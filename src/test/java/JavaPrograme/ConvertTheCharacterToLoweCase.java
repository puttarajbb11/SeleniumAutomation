package JavaPrograme;

public class ConvertTheCharacterToLoweCase {

    public static void main(String[] args) {
        String word="bangaluru is beautiful";
        String word1="BANGALURU IS BEAUTIFUL";

        System.out.println("After converting to Upper case ---->"+word.toUpperCase());
        System.out.println("After converting to lower case ---->"+word1.toLowerCase());
    }
}